#include <iostream>

int main()
{
    int x = 24;
    int y = 12;

    // Addition(+)
    std::cout << "Addition: " << x + y << std::endl; // 36

    // Subtraction(-)
    std::cout << "Subtraction: " << x - y << std::endl; // 12

    // Multiplication(*)
    std::cout << "Multiplication: " << x * y << std::endl; // 288

    // Division(/)
    std::cout << "Division: " << x / y << std::endl; // 2

    // Modulus(%)
    std::cout << "Modulus(Remainder): " << x % y << std::endl; // 0
}